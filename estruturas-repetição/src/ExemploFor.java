public class ExemploFor {
    public static void main(String[] args) {
        
        /*ESTRUTURA DO FOR EM JAVA
         *O for é usado quando já sabemos quantas vezes queremos repetir algo.
         *Ele tem 3 partes: inicialização; condição; incremento.
         */
        for (int carneirinhos = 0; carneirinhos <= 20; carneirinhos++){
            System.err.println(carneirinhos + " carneirinho...");
            
        /*
         *carneirinhos = 0; -> inicializa a variável de controle (começa do 0)
         *carneirinhos <= 20; -> condição de parada (enquanto for verdadeira, continua)
         *carneirinhos ++; -> incremento (aumenta i em 1 a cada volta)
         */
        
        }
    }
}